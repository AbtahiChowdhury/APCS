public class TwoPointNine
{
    public static void main (String[] args)
    {
        double r = 6.5;
        double a = 0.8;
        double p = 3.1416;
        System.out.println ("If r = " + r);
        System.out.println ("then");
        System.out.println ("V = " + a*p*r*r*r);
        System.out.println ("SA = " + 4*p*r*r);
    }
}
